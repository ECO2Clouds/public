package eu.eco2clouds.ac.monitor;

import javax.xml.bind.annotation.XmlAccessType;
import javax.xml.bind.annotation.XmlAccessorType;
import javax.xml.bind.annotation.XmlElement;
import javax.xml.bind.annotation.XmlRootElement;

/**
 *
 *  
 */
@XmlAccessorType(XmlAccessType.FIELD)
@XmlRootElement(name = "vm", namespace = Report.E2C_NAMESPACE)
public class ItemValue {

    @XmlElement(name = "timestamp", namespace = Report.E2C_NAMESPACE)
    long timestamp;
    @XmlElement(name = "value", namespace = Report.E2C_NAMESPACE)
    double value;

    public ItemValue() {
        
    }
    
    public ItemValue(long timestamp, double value) {
        this.timestamp = timestamp;
        this.value = value;
    }

    public long getTimestamp() {
        return timestamp;
    }

    public void setTimestamp(long timestamp) {
        this.timestamp = timestamp;
    }

    public double getValue() {
        return value;
    }

    public void setValue(double value) {
        this.value = value;
    }
    
    
    
}
